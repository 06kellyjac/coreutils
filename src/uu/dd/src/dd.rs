// ----- Types -----
enum SrcStat
{
    Read(usize),
    EOF,
}

#[derive(Debug)]
enum InternalError
{
    WrongInputType,
    WrongOutputType,
}

impl std::fmt::Display for InternalError
{
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(f, "Internal dd error")
    }
}

impl Error for InternalError {}

struct Input<R: Read>
{
    src: R,
    ibs: usize,
    output_progress: bool,
}

impl<R: Read> Read for Input<R>
{
    fn read(&mut self, buf: &mut [u8]) -> io::Result<usize>
    {
        self.src.read(buf)
    }
}

impl Input<io::Stdin>
{
    fn new(matches: &getopts::Matches) -> Result<Self, Box<dyn Error>>
    {
        let ibs: usize = ddargs::parse_ibs(matches)?;
        let output_progress = ddargs::parse_progress_level(matches)?;

        Ok(
            Input {
                src: io::stdin(),
                ibs,
                output_progress,
            }
        )

    }
}

impl Input<File>
{
    fn new(matches: &getopts::Matches) -> Result<Self, Box<dyn Error>>
    {
        let ibs: usize = ddargs::parse_ibs(matches)?;
        let output_progress = ddargs::parse_progress_level(matches)?;

        if let Some(fname) = matches.opt_str("if")
        {
            Ok(Input {
                src: File::open(fname)?,
                ibs,
                output_progress,
            })
        }
        else
        {
            Err(Box::new(InternalError::WrongInputType))
        }

    }
}

impl<R: Read> Input<R>
{
    fn fill_n(&mut self, buf: &mut [u8], obs: usize) -> Result<SrcStat, Box<dyn Error>>
    {
        let ibs = self.ibs;
        let mut bytes_read = 0;

        for n in 0..(obs/ibs) {
            // fill an ibs-len slice from src
            let this_read = self.read(&mut buf[n*ibs..(n+1)*ibs])?;

            if this_read != 0 {
                bytes_read += this_read;
            } else {
                break;
            }
        }

        if bytes_read != 0 {
            Ok(SrcStat::Read(bytes_read))
        } else {
            Ok(SrcStat::EOF)
        }
    }
}

struct Output<W: Write>
{
    dst: W,
    obs: usize,
    conv_table: Option<ConversionTable>,
}

impl Output<io::Stdout> {
    fn new(matches: &getopts::Matches) -> Result<Self, Box<dyn Error>>
    {
        let obs: usize = ddargs::parse_obs(matches)?;
        let conv_table = ddargs::parse_conv_table(matches)?;

        Ok(
            Output {
                dst: io::stdout(),
                obs,
                conv_table,
            }
        )
    }
}

impl Output<File> {
    fn new(matches: &getopts::Matches) -> Result<Self, Box<dyn Error>>
    {
        let obs: usize = ddargs::parse_obs(matches)?;
        let conv_table = ddargs::parse_conv_table(matches)?;

        if let Some(fname) = matches.opt_str("if")
        {
            Ok(Output {
                dst: File::open(fname)?,
                obs,
                conv_table,
            })
        }
        else
        {
            Err(Box::new(InternalError::WrongOutputType))
        }
    }
}

impl<W: Write> Write for Output<W>
{
    fn write(&mut self, buf: &[u8]) -> io::Result<usize>
    {
        if let Some(ct) = self.conv_table
        {
            let mut cbuf = vec![0; buf.len()];
           
            for (idx, byte) in buf.iter().enumerate()
            {
                cbuf[idx] = ct[*byte as usize]
            }

            self.dst.write(&cbuf)
        }
        else
        {
            self.dst.write(buf)
        }
    }

    fn flush(&mut self) -> io::Result<()>
    {
        self.dst.flush()
    }
}

